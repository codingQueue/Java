/**
 * Demonstration of Bitwise operators.
 * @author CodingQueue
 */
public class BitwiseOperators{
    public static void main(String[] args) {
        int a = 6, b = 5;

        System.out.println("a & b -> " + (a&b));
        System.out.println("a | b -> " + (a|b));
        System.out.println("a ^ b -> " + (a^b));
        System.out.println("~a -> " + (~a));
    }
}