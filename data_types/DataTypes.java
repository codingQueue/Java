/**
In this program all primitive data types are demonstrated.
@author CodingQueue
*/
public class DataTypes{
    public static void main(String args[]){
        //Syntax - <data type> <variable name> = <value>;

        //Boolean - boolean - 1 bit - true or false
        boolean bool = true;

        //Character - char - 1 byte
        char c = 'a';

        //String
        String str = "This is java tutorial";

        //Numneric - Integer
        //byte - 1 byte - (-128 to 127)
        byte b = 125;
        //short - 2 bytes  - (-32,768 to 32,767)
        short s = 6253;

        //int - 4 bytes
        int a = 256984;

        //long - 8 bytes
        long l = 485656512;


        //Numneric - FloatingPoint
        //flaot - 4 bytes
        float f = 1.45;
        //double - 8 bytes
        double d = 1.45;
    }
}